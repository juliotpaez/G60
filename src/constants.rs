#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_reverse_mapping() {
        for (index, c) in G60_TO_CHAR.iter().enumerate() {
            let value = CHAR_TO_G60[*c as usize];

            assert_eq!(index, value as usize, "Incorrect for {}", c)
        }
    }
}
